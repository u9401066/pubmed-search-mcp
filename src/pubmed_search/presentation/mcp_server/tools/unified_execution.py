"""Execution orchestration for unified_search.

Design:
    This module runs the plan generated by unified_planning.py. It coordinates
    source execution, deep-search expansion, enrichment, aggregation, and
    session updates while keeping the outer tool handler thin.

Maintenance:
    Add runtime orchestration here, not in planning or formatting modules.
    Preserve the clear handoff from UnifiedSearchPlan to execution result so
    retries and source metrics remain testable.
"""

